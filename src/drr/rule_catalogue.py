# ---------------------------------------------------------------------------
# Mapping: txr_automation validator name → DRR rule name(s)
# ---------------------------------------------------------------------------

VALIDATOR_TO_RULES: dict[str, list[str]] = {
    "validate-buyer": ["BuyerSeller_Buyer"],
    "validate-seller": ["BuyerSeller_Seller"],
    "validate-inconsistent-buyer": ["BuyerSeller_Buyer"],
    "validate-inconsistent-seller": ["BuyerSeller_Seller"],
    "validate-ftbdm": ["InvestmentDecisionWithinFirm"],
    "validate-ftsdm": ["InvestmentDecisionWithinFirm"],
    "validate-incorrect-net-amount": ["Quantity", "Price"],
    "validate-non-zero-net-qty": ["Quantity"],
    "validate-non-zero-net-amt": ["Quantity", "Price"],
    "validate-incorrect-time": ["TradingDateTime"],
}


def get_rules_for_validator(validator_name: str) -> list[RuleReference]:
    """Return the DRR rule references that correspond to a txr validator."""
    return [
        CATALOGUE[r]
        for r in VALIDATOR_TO_RULES.get(validator_name, [])
        if r in CATALOGUE
    ]
