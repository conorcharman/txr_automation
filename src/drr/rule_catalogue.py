"""MiFIR RTS 22 rule catalogue.

Static catalogue of MiFIR Article 26 / RTS 22 (Commission Delegated Regulation
(EU) 2017/590) reporting rules, extracted from the ISDA DRR model file
``regulation-esma-mifir-rule.rosetta`` (distribution 6.34.1).

Every entry corresponds to a ``reporting rule`` or ``eligibility rule`` in the
DRR Rosetta DSL.  All rules are currently ``extract "Not Modelled"`` stubs in
DRR 6.34.1; the catalogue captures the regulatory references so that
txr_automation validation output carries DRR-compatible traceability now, with
rule logic to be supplied by a future DRR release.

Structure of each rule entry:
    rule_name       – Rosetta DSL rule identifier
    field_number    – RTS 22 Annex I Table 2 field number
    field_name      – Descriptive name from the annex
    regulation      – Source regulation reference
    provision       – Exact text from Commission Delegated Regulation (EU) 2017/590
"""

from dataclasses import dataclass


@dataclass(frozen=True)
class RuleReference:
    """A single DRR regulatory rule reference."""

    rule_name: str
    field_number: str
    field_name: str
    regulation: str
    provision: str


